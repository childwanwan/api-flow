CREATE TABLE IF NOT EXISTS api_config (
    id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    group_no VARCHAR(64),
    api_code VARCHAR(64) NOT NULL,
    api_name VARCHAR(128) NOT NULL,
    api_description VARCHAR(512),
    status VARCHAR(32) NOT NULL DEFAULT 'ENABLED',
    request_timeout_ms BIGINT NOT NULL DEFAULT 30000,
    auto_retry_count INT NOT NULL DEFAULT 64,
    retry_interval_ms BIGINT NOT NULL DEFAULT 5000,
    rate_limit_config VARCHAR,
    max_queue_size INT NOT NULL DEFAULT 100000,
    filter_rules VARCHAR,
    plugin_config VARCHAR,
    receipt_config VARCHAR,
    extra_config VARCHAR,
    create_time_ms BIGINT NOT NULL,
    update_time_ms BIGINT NOT NULL,
    create_operator VARCHAR(64),
    update_operator VARCHAR(64),
    deleted TINYINT NOT NULL DEFAULT 0,
    version INT NOT NULL DEFAULT 0,
    UNIQUE KEY uk_api_config_code_deleted (api_code, deleted)
);

CREATE TABLE IF NOT EXISTS api_task (
    id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    task_no VARCHAR(64) NOT NULL,
    source VARCHAR(64) NOT NULL DEFAULT 'API',
    group_no VARCHAR(64),
    api_code VARCHAR(64) NOT NULL,
    api_name VARCHAR(128),
    action_type VARCHAR(32) NOT NULL DEFAULT 'ASYNC',
    status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
    interrupt_flag TINYINT NOT NULL DEFAULT 0,
    compensate_status VARCHAR(32) NOT NULL DEFAULT 'NONE',
    compensate_retry_count INT NOT NULL DEFAULT 0,
    compensate_next_retry_time_ms BIGINT,
    priority INT NOT NULL DEFAULT 10,
    request_context VARCHAR NOT NULL,
    exec_info VARCHAR,
    response_data VARCHAR,
    expire_time_ms BIGINT,
    receipt_config VARCHAR,
    receipt_info VARCHAR,
    retry_count INT NOT NULL DEFAULT 0,
    max_retry_count INT NOT NULL DEFAULT 64,
    next_retry_time_ms BIGINT,
    create_time_ms BIGINT NOT NULL,
    start_execute_time_ms BIGINT,
    end_execute_time_ms BIGINT,
    execute_duration_ms BIGINT,
    cancel_time_ms BIGINT,
    cancel_reason VARCHAR(512),
    canceled_by VARCHAR(64),
    update_time_ms BIGINT NOT NULL,
    deleted TINYINT NOT NULL DEFAULT 0,
    version INT NOT NULL DEFAULT 0,
    UNIQUE KEY uk_api_task_no_deleted (task_no, deleted)
);

CREATE INDEX IF NOT EXISTS idx_task_status_interrupt ON api_task (status, interrupt_flag, priority, create_time_ms);
CREATE INDEX IF NOT EXISTS idx_task_retry ON api_task (status, next_retry_time_ms);
